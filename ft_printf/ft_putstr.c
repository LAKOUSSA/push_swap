#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;
	int	len;

	len = 0;
	if (!s)
		return (len);
	i = 0;
	while (s[i])
	{
		len += ft_putchar(s[i]);
		i++;
	}
	return (len);
}
